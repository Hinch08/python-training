#formatted strings
#also known as f-string and curly braces{}
f_name="ken" 
l_name="sharon"
print("This is ken mugambi")
print(f"{f_name}{l_name}is a student")
print(f"{f_name}{l_name} is a student")
print(f"{f_name}{l_name}".upper())
print(f"{f_name}{l_name}".lower())
print(f"{f_name}{l_name}".replace('e','l'))
print(f"{f_name}{l_name}".count('y'))
print(f"{f_name}{l_name}".endswith('n'))
print(f"{f_name}{l_name}".startswith('s'))
print(len(f"{f_name}{l_name}"))
